
function setDC_51(){
	
	if(document.forms[0].dc_selected_51.checked==false){
		document.forms[0].dc_ec_51.value = "";
		document.forms[0].dc_ec_valid_51.value = "";

		document.forms[0].dc_ec_51.readOnly = true;
		document.forms[0].dc_ec_valid_51.readOnly = true;

		//document.forms[0].dc_ec_51.nocheck = "true";
		//document.forms[0].dc_ec_valid_51.nocheck = "true";
	}		
	else{
		document.forms[0].dc_ec_51.readOnly = false;
		document.forms[0].dc_ec_valid_51.readOnly = false;

		document.forms[0].dc_ec_51.nocheck = "false";
		document.forms[0].dc_ec_valid_51.nocheck = "false";
	}
}

